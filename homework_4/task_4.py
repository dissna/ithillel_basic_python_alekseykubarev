text = input('Введіть будь-який текст: ')
print('Третій символ цього рядка: ', text[2])
print('Передостанній символ цього рядка: ', text[-2])
print("Перші п'ять символів цього рядка: ", text[:5])
print('Весь рядок, крім двох останніх символів: ', text[:-2])
print('Усі символи з парними індексами: ', text[::2])
print('Усі символи з непарними індексами: ', text[1::2])
print('Усі символи у зворотному порядку: ', text[::-1])
print('Усі символи рядка через один у зворотному порядку, починаючи з останнього: ', text[::-2])
print('Довжина цього рядка: ', len(text))